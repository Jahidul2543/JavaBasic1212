package arraydemo;

public class ArrayDemo2 {

    public static void main(String[] args) {
        // Declare an array of length 6
        int[] studentId = new int[7];
        // Storing value in an array
        studentId[0] = 200;
        studentId[1] = 300;
        studentId[2] = 3;
        studentId[3] = 4;
        studentId[4] = 55;
        studentId[5] = 33;
        studentId[6] = 440;
        // Retrieving values from an array
        System.out.println(" Index 0 value: " + studentId[0]);
        System.out.println(" Index 1 value: " + studentId[1]);
        System.out.println(" Index 2 value: " + studentId[2]);
        System.out.println(" Index 3 value: " + studentId[3]);
        System.out.println(" Index 4 value: " + studentId[4]);
        System.out.println(" Index 5 value: " + studentId[5]);
        System.out.println(" Index 6 value: " + studentId[6]);

    }
}
